// 添加词组匹配功能
function findLongestMatch(text, startIndex) {
    let longestMatch = '';
    
    // 优先检查优先级词组
    for (const phrase of phrasesPriority) {
        if (text.startsWith(phrase, startIndex) && phrase.length > longestMatch.length) {
            longestMatch = phrase;
        }
    }
    
    // 如果没有找到优先级词组，检查其他词组
    if (!longestMatch) {
        for (const key of Object.keys(synonymsDB)) {
            if (key.length > 1 && text.startsWith(key, startIndex) && key.length > longestMatch.length) {
                longestMatch = key;
            }
        }
    }
    
    return longestMatch;
}

